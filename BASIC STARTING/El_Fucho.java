import java.util.*;
public class El_Fucho {
    
    public static void main(String[] args) {
        @SuppressWarnings("resource")
        Scanner sc = new Scanner(System.in);
        int n=sc.nextInt();
        for (int i = 0; i < n; i++) 
        {
            int a=sc.nextInt();
            System.out.println(2*a-2);
        }
    }
}